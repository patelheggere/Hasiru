package com.ksrsac.hasiru.model;

public class Lat_Longmodel {
    private String lat;
    private String lng;


    public Lat_Longmodel(String lat, String lng) {

        this.lat = lat;
        this.lng = lng;
    }

    public String getLat() {
        return lat;
    }

    public String getLng() {
        return lng;
    }

}

